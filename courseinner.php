                         $(".moreless").click(function() {
                           var thisEl = $(this);
                           var cT = thisEl.closest(".truncate-text");
                           var tX = ".truncate-text";

                           if (thisEl.hasClass("less")) {
                             cT.prev(tX).toggle();
                             cT.slideToggle();
                           } else {
                             cT.toggle();
                             cT.next(tX).fadeToggle();
                           }
                           return false;
                         });
